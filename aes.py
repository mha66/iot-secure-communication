    @classmethod
    def sub_byte(cls, byte: np.uint8, inverse=False):
        row = byte >> 4    # Isolate the leftmost 4 bits
        col = byte & 0x0F  # Isolate the rightmost 4 bits
        return cls.S_BOX[row, col] if not inverse else cls.INV_S_BOX[row, col]
    
    @classmethod
    def generate_rc(cls, rounds):
        RC = np.zeros(rounds+1, dtype=np.uint8)
        RC[1] = 0x01
        for j in range(2, rounds+1):
            RC[j] = mul_GF256(RC[j-1], 0x02)
        return RC


    def __init__(self, key=None, rounds = 10):
        if key is None:
            self.key = np.frombuffer(os.urandom(16), dtype=np.uint8).reshape((4,4), order='F') # Generate a random 16-byte key if not provided
        elif isinstance(key, bytes):
            self.key = np.frombuffer(key, dtype=np.uint8).reshape((4,4), order='F')
        else:
            self.key = np.array(key, dtype=np.uint8).reshape((4,4), order='F')
        
        self.rounds = rounds
        self.RC = self.generate_rc(rounds)
        self.round_key_words = self.expand_key(self.key)
    
    def g(self, word, round):
        g_word = np.roll(word, shift=-1)
        g_word = self.sub_bytes(g_word)
        g_word = g_word ^ np.array([self.RC[round], 0, 0, 0], dtype=np.uint8)
        return g_word
    
    def expand_key(self, key):
        round_key_words = np.zeros((4*(self.rounds+1), 4), dtype=np.uint8)
        # The first 4 words of the expanded key are just the original key
        for j in range(4):
            round_key_words[j] = key[:, j] 
        # Generate the remaining words using the key expansion algorithm
        for i in range(1, self.rounds + 1):
            g_word = self.g(round_key_words[4*i-1], round=i)
            round_key_words[4*i] = round_key_words[4*i-4] ^ g_word
            for j in range(3):
                round_key_words[4*i + j + 1] = round_key_words[4*i - 4 + j + 1] ^ round_key_words[4*i + j]

        return round_key_words