def mul_GF256(a : np.uint8, b : np.uint8):
    if bin(a).count('1') < bin(b).count('1') or (bin(a).count('1') == bin(b).count('1') and a<b) :
        x = b
        y = a
    else:
        x = a
        y = b
        
    shift_results = []
    x_intermediate = np.uint16(x) # Use uint16 to detect overflow during shifts
    y_binary = bin(y)[2:]  # Exclude '0b' from the binary string 0b100011011 --> 100011011
    ones_count = 0
    # Iterate over y_binary in reverse
    for i in range(len(y_binary)-1, -1, -1):
        # Don't shift for bit 0
        if i != len(y_binary)-1:
            x_intermediate = x_intermediate << 1
        # XOR with 100011011 if result exceeds 8 bits 
        if x_intermediate > 0xFF:
            x_intermediate = x_intermediate ^ 0x11B
        # Record intermediate result if at a 1
        if y_binary[i] == '1':
            shift_results.append(x_intermediate)
            ones_count += 1
        # Break if all the 1's are passed over in y_binary
        if ones_count == y_binary.count('1'):
            break

    # XOR the recorded results
    result = np.uint8(0x00)
    for byte in shift_results:
        result = result ^ byte
    return result

